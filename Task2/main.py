import markovify

# Розширений корпус тексту для навчання моделі
text = """
Енергетичний проект передбачає будівництво нової вітрової електростанції потужністю 100 МВт.
Очікується, що впровадження сучасних акумуляторних систем дозволить знизити витрати на генерацію електроенергії.
Підприємство планує встановити понад 5000 сонячних панелей у рамках стратегії екологічного розвитку.
Урядова програма підтримки альтернативної енергетики забезпечить фінансування у розмірі 20 мільйонів доларів.
Нова технологія дозволить підвищити ККД виробництва електроенергії на 18%.
Використання гідроакумулюючих станцій забезпечить стабільність мережі та мінімізує пікові навантаження.
Розвиток водневої енергетики є ключовим напрямом стратегічного плану держави.
Планується впровадження технологій зберігання енергії, що дозволить скоротити викиди вуглецю.
Компанія розробляє стратегію зменшення викидів CO2 шляхом переходу на відновлювані джерела енергії.
Завдяки сучасним акумуляторам нові вітрові електростанції працюватимуть стабільніше навіть при змінних погодних умовах.
У межах проекту планується встановлення 50 вітряків, що генеруватимуть 200 ГВт-год електроенергії щороку.
Нове дослідження показало, що сонячні панелі з удосконаленими фотоелементами можуть підвищити виробництво енергії на 25%.
"""

# Створення Markov-моделі
text_model = markovify.Text(text, state_size=3)

# Генерація згенерованого тексту
generated_report = text_model.make_sentence(tries=200, test_output=False)

# Генерація більш стислого звіту
if generated_report:
    # Обрізаємо текст до основних тверджень і поєднуємо їх у коротке речення
    report_parts = generated_report.split(".")[:2]  # Вибираємо перші два ключові твердження
    short_summary = " ".join(report_parts) + "."  # З'єднуємо і додаємо крапку
    print("\nКороткий опис звіту:")
    print(short_summary)
else:
    print("\nНе вдалося згенерувати текст. Додайте більше вхідних даних.")

